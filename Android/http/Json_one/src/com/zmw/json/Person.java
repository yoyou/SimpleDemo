package com.zmw.json;

import java.util.List;


public class Person {

		private String name;
		private String url;
		private int age;
		private List<SchoolInfo> schoolinfo;
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public String getUrl() {
			return url;
		}
		public void setUrl(String url) {
			this.url = url;
		}
		public int getAge() {
			return age;
		}
		public void setAge(int age) {
			this.age = age;
		}
		public List<SchoolInfo> getList() {
			return schoolinfo;
		}
		public void setList(List<SchoolInfo> schoolinfo) {
			this.schoolinfo = schoolinfo;
		}

		
		
}
